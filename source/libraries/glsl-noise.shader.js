export const fractal = ({passes = 3, noiseFunction = "noise", dimensions = 2}) => `
  const int FRACTAL_PASSES = ${passes};

  float fractal (in vec${dimensions} coordinates) {    
    float G = 0.5;
    float frequency = 1.0;
    float amplitude = 1.0;
    float value = 0.0;

    for(int i = 0; i < FRACTAL_PASSES; i++) {
      value += amplitude*${noiseFunction}(frequency * coordinates);
      frequency *= 2.0;
      amplitude *= G;
    }
    return value;
  }
`;